#include<stdio.h>
#include<conio.h>
int main()
{
    int a;
    printf("enter a any value for increment operator=");
    scanf("%d",&a);
    printf("\nThe value of a=%d",a);
    printf("\nThe value of ++a=%d",++a);
    printf("\nThe value of a++=%d",a++);
    printf("\nThe value of a=%d",a);
    printf("\n\nenter a any value for decrement operator=");
    scanf("%d",&a);
    printf("\nThe value of a=%d",a);
    printf("\nThe value of --a=%d",--a);
    printf("\nThe value of a--=%d",a--);
    printf("\nThe value of a=%d",a);
    return 0;
}
